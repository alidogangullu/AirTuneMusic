#!/usr/bin/env node
/**
 * Injects EXPO_PUBLIC_APPLE_MUSIC_TOKEN from .env.local into a generated TS file.
 * Run before bundling so the app can read the token without pulling dotenv into the Metro bundle.
 */

import {readFileSync, writeFileSync, existsSync, mkdirSync} from 'node:fs';
import {resolve, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const envPath = resolve(root, '.env.local');
const outPath = resolve(root, 'src/config/appleMusicToken.generated.ts');

let token = '';
try {
  if (existsSync(envPath)) {
    const env = readFileSync(envPath, 'utf8');
    const m = env.match(/^\s*EXPO_PUBLIC_APPLE_MUSIC_TOKEN\s*=\s*(.+)/m);
    if (m) {
      const raw = m[1].trim();
      token =
        raw.startsWith('"') || raw.startsWith("'") ? raw.slice(1, -1) : raw;
    }
  }
} catch {
  // .env.local missing or not readable (e.g. in CI/sandbox); keep token empty
}

const content = `/**
 * Generated by scripts/inject-apple-music-token.mjs – do not edit.
 * Token is read from .env.local (EXPO_PUBLIC_APPLE_MUSIC_TOKEN) at build time.
 */
export const APPLE_MUSIC_DEVELOPER_TOKEN: string = ${JSON.stringify(token)};
`;

const configDir = dirname(outPath);
if (!existsSync(configDir)) {
  mkdirSync(configDir, {recursive: true});
}
writeFileSync(outPath, content, 'utf8');
